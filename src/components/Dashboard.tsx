import { useQuery } from "convex/react";
import { api } from "../../convex/_generated/api";
import { NetworkStats } from "./NetworkStats";
import { LatestBlocks } from "./LatestBlocks";
import { LatestTransactions } from "./LatestTransactions";
import { ChartContainer } from "./ChartContainer";

export function Dashboard() {
  const networkStats = useQuery(api.blockchain.getNetworkStats);
  const latestBlocks = useQuery(api.blockchain.getLatestBlocks, {
    paginationOpts: { numItems: 10, cursor: null }
  });
  const latestTransactions = useQuery(api.blockchain.getLatestTransactions, {
    paginationOpts: { numItems: 10, cursor: null }
  });

  return (
    <div className="space-y-8">
      {/* Network Overview */}
      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
        <div className="bg-white rounded-lg shadow p-6">
          <div className="flex items-center">
            <div className="p-2 bg-blue-100 rounded-lg">
              <svg className="w-6 h-6 text-blue-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
              </svg>
            </div>
            <div className="ml-4">
              <p className="text-sm font-medium text-gray-600">Gas Price</p>
              <p className="text-2xl font-semibold text-gray-900">
                {networkStats?.gasPrice ? `${parseInt(networkStats.gasPrice) / 1e9} Gwei` : '---'}
              </p>
            </div>
          </div>
        </div>

        <div className="bg-white rounded-lg shadow p-6">
          <div className="flex items-center">
            <div className="p-2 bg-green-100 rounded-lg">
              <svg className="w-6 h-6 text-green-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 19v-6a2 2 0 00-2-2H5a2 2 0 00-2 2v6a2 2 0 002 2h2a2 2 0 002-2zm0 0V9a2 2 0 012-2h2a2 2 0 012 2v10m-6 0a2 2 0 002 2h2a2 2 0 002-2m0 0V5a2 2 0 012-2h2a2 2 0 012 2v14a2 2 0 01-2 2h-2a2 2 0 01-2-2z" />
              </svg>
            </div>
            <div className="ml-4">
              <p className="text-sm font-medium text-gray-600">Latest Block</p>
              <p className="text-2xl font-semibold text-gray-900">
                {networkStats?.blockNumber?.toLocaleString() || '---'}
              </p>
            </div>
          </div>
        </div>

        <div className="bg-white rounded-lg shadow p-6">
          <div className="flex items-center">
            <div className="p-2 bg-purple-100 rounded-lg">
              <svg className="w-6 h-6 text-purple-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8c-1.657 0-3 .895-3 2s1.343 2 3 2 3 .895 3 2-1.343 2-3 2m0-8c1.11 0 2.08.402 2.599 1M12 8V7m0 1v8m0 0v1m0-1c-1.11 0-2.08-.402-2.599-1" />
              </svg>
            </div>
            <div className="ml-4">
              <p className="text-sm font-medium text-gray-600">Total TXs</p>
              <p className="text-2xl font-semibold text-gray-900">
                {networkStats?.totalTransactions?.toLocaleString() || '---'}
              </p>
            </div>
          </div>
        </div>

        <div className="bg-white rounded-lg shadow p-6">
          <div className="flex items-center">
            <div className="p-2 bg-orange-100 rounded-lg">
              <svg className="w-6 h-6 text-orange-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 20h5v-2a3 3 0 00-5.356-1.857M17 20H7m10 0v-2c0-.656-.126-1.283-.356-1.857M7 20H2v-2a3 3 0 015.356-1.857M7 20v-2c0-.656.126-1.283.356-1.857m0 0a5.002 5.002 0 019.288 0M15 7a3 3 0 11-6 0 3 3 0 016 0zm6 3a2 2 0 11-4 0 2 2 0 014 0zM7 10a2 2 0 11-4 0 2 2 0 014 0z" />
              </svg>
            </div>
            <div className="ml-4">
              <p className="text-sm font-medium text-gray-600">Active Addresses</p>
              <p className="text-2xl font-semibold text-gray-900">
                {networkStats?.activeAddresses?.toLocaleString() || '---'}
              </p>
            </div>
          </div>
        </div>
      </div>

      {/* Charts */}
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-8">
        <ChartContainer title="Gas Price Trend" />
        <ChartContainer title="Transaction Volume" />
      </div>

      {/* Latest Data */}
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-8">
        <LatestBlocks blocks={latestBlocks?.page || []} />
        <LatestTransactions transactions={latestTransactions?.page || []} />
      </div>
    </div>
  );
}
